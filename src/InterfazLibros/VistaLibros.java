    private void btnRegistrarLibrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarLibrosActionPerformed
        // TODO add your handling code here:
        String titulo = txtTitulo.getText();
        String autor = txtAutor.getText();
        String AnioP= txtAPublicacion.getText();
        String Disp = txtDisponible.getText();
        LibroPOO libros = new LibroPOO();
        
        libros.setAutor(autor);
        libros.setAnioPublicacion(Integer.parseInt(AnioP));
        libros.setDisponible(Disp);
        libros.setTitulo(titulo);
        
       SGProducto.listaLibros.agregarLibros(libros);
    }//GEN-LAST:event_btnRegistrarLibrosActionPerformed

