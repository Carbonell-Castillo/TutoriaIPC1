    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String correo = txtCorreo.getText();
        String contrasenia = txtContrasenia.getText();
        String edad = txtEdad.getText();
        
        Usuario usuario = new Usuario();
        
        usuario.setNombre(nombre);
        usuario.setCorreo(correo);
        usuario.setContrasena(contrasenia);
        usuario.setEdad(Integer.parseInt(edad));
        
        SG.listaUsuarios.agregarUsuario(usuario);

        System.out.println("Se aguardo exitosamente");
        JOptionPane.showMessageDialog(null, "Se aguardo exitosamente");
        JOptionPane.showConfirmDialog(null, "Deseas conitnuar?");
        JOptionPane.showInputDialog("Ingrese su codigo");
                ImageIcon icon = null;
        try {
            icon = new ImageIcon(new URL("http://www.tutorialspoint.com/images/css.png"));
        } catch (MalformedURLException ex) {
            Logger.getLogger(FRMRegistro.class.getName()).log(Level.SEVERE, null, ex);
        }
        JLabel label = new JLabel(icon);
        JPanel panel = new JPanel(new GridBagLayout());
        panel.add(label);
        JPanel textPanel = new JPanel(new GridLayout(5, 3));
        for (int i = 0; i < 10; i++) {
            textPanel.add(new JLabel("Learn CSS"));
        }
        JPanel panel2 = new JPanel(new BorderLayout());
        panel2.add(textPanel);
        panel2.add(panel, BorderLayout.EAST);
        JOptionPane.showMessageDialog(null, panel2, "Course",JOptionPane.DEFAULT_OPTION);
                
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

